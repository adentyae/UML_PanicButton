package panicbutton;


public class DetailKejadian {
    
    String idkejadian;
    String nama_kejadian;
    String lokasi;
    String lev_kejadian;
    String tgl_kejadian;
    
    public String getIdkejadian() {
        return idkejadian;
    }

    public void setIdkejadian(String idkejadian) {
        this.idkejadian = idkejadian;
    }

    public String getNama_kejadian() {
        return nama_kejadian;
    }

    public void setNama_kejadian(String nama_kejadian) {
        this.nama_kejadian = nama_kejadian;
    }

    public String getLokasi() {
        return lokasi;
    }

    public void setLokasi(String lokasi) {
        this.lokasi = lokasi;
    }

    public String getLev_kejadian() {
        return lev_kejadian;
    }

    public void setLev_kejadian(String lev_kejadian) {
        this.lev_kejadian = lev_kejadian;
    }

    public String getTgl_kejadian() {
        return tgl_kejadian;
    }

    public void setTgl_kejadian(String tgl_kejadian) {
        this.tgl_kejadian = tgl_kejadian;
    }
    
    
    
}
